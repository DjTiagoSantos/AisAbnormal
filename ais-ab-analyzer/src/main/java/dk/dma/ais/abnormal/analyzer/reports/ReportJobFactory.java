package dk.dma.ais.abnormal.analyzer.reports;

import com.google.inject.Inject;
import com.google.inject.Injector;
import org.quartz.Job;
import org.quartz.JobDetail;
import org.quartz.Scheduler;
import org.quartz.SchedulerException;
import org.quartz.spi.JobFactory;
import org.quartz.spi.TriggerFiredBundle;

/**
 * This class performs integration between Google Guice and Quartz Scheduler.
 */
public class ReportJobFactory implements JobFactory {

    private final Injector injector;

    @Inject
    public ReportJobFactory(final Injector injector) {
        this.injector = injector;
    }

    @Override
    public Job newJob(TriggerFiredBundle triggerFiredBundle, Scheduler scheduler) throws SchedulerException {
        JobDetail jobDetail = triggerFiredBundle.getJobDetail();
        Class jobClass = jobDetail.getJobClass();
        return (Job) injector.getInstance(jobClass);
    }
}